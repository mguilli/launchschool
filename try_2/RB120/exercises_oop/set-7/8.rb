class Card
  include Comparable
  attr_reader :rank, :suit

  FACE_VALUES = %w(Jack Queen King Ace).zip(11..14).to_h
  SUIT_VALUE = %w(Diamonds Clubs Hearts Spades) # Further exploration

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def to_s
    "#{rank} of #{suit}"
  end

  def value
    FACE_VALUES[rank] || rank
  end

  def <=>(other)
    # value <=> other.value
    [value, SUIT_VALUE.index(suit)] <=> [other.value, SUIT_VALUE.index(other.suit)]
  end
end

cards = [Card.new(2, 'Hearts'),
  Card.new(10, 'Diamonds'),
  Card.new('Ace', 'Clubs')]
puts cards
puts cards.min == Card.new(2, 'Hearts')
puts cards.max == Card.new('Ace', 'Clubs')

cards = [Card.new(5, 'Hearts')]
