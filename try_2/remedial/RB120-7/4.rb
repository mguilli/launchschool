class CircularQueue
  def initialize(size_limit)
    @size_limit = size_limit
    @buffer = []
  end

  def dequeue
    @buffer.shift
  end

  def enqueue(value)
    @buffer << value
    dequeue if @buffer.size > @size_limit
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5
puts queue.dequeue == 6
puts queue.dequeue == 7
puts queue.dequeue == nil

